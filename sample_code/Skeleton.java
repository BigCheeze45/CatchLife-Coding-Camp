
/**
 * Each program starts with a class deceleration
 * then the class name (Skeleton in this case).
 * 
 * Inside of the class (between the opening
 * and closing curly braces) will be your program
 * code that will be use when the program running.
 */
public class Skeleton { // Class deceleration starts

	/**
	 * The main method serves as the entry point into
	 * your program. Because of that your main method
	 * will always be written the same way.
	 */
	public static void main(String[] args) { // main method starts
		// Code to start running your program must live
		// inside of the main method.
		// In this case we are just putting some text
		// on the screen
		System.out.println("Hello, world!");

	} // main method ends

} // Class deceleration end
